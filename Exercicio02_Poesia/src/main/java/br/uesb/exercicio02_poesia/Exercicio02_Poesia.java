package br.uesb.exercicio02_poesia;

public class Exercicio02_Poesia {
    public static void main(String[] args) {
        // Declaração das variáveis do tipo String (texto)
        String acao = "canto";
        String identidade = "sou poeta";
        
        // Exibição dos versos utilizando concatenação (+)
        System.out.println("Eu " + acao + " porque o instante existe");
        System.out.println("e a minha vida está completa.");
        System.out.println("Não sou alegre nem sou triste:");
        
        // Exibição da identidade com o ponto final concatenado
        System.out.println(identidade + ".");
    }
}